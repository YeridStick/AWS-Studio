/**
 * Generar outputs útiles
 */
function generateOutputs(resources) {
  const outputs = {}
  
  if (resources.ALB) {
    outputs.LoadBalancerURL = {
      Description: 'URL of the load balancer',
      Value: { 'Fn::Join': ['', ['http://', { 'Fn::GetAtt': ['ALB', 'DNSName'] }]] }
    }
  }
  
  if (resources.RDS) {
    outputs.DatabaseEndpoint = {
      Description: 'RDS Database endpoint',
      Value: { 'Fn::GetAtt': ['RDS', 'Endpoint.Address'] }
    }
  }
  
  if (resources.ECSCluster) {
    outputs.ClusterName = {
      Description: 'ECS Cluster Name',
      Value: { Ref: 'ECSCluster' }
    }
  }
  
  return outputs
}

/**
 * Sanitizar nombre para CloudFormation
 */
function sanitizeResourceName(name) {
  return name
    .replace(/[^a-zA-Z0-9]/g, '')
    .replace(/^[0-9]/, 'N')
    .substring(0, 64)
}

/**
 * Obtener AMI más reciente
 */
function getLatestAMI(os) {
  const amis = {
    'amazon-linux-2': 'ami-0c55b159cbfafe1f0',
    'amazon-linux-2023': 'ami-0c02fb55956c7d316',
    'ubuntu-22': 'ami-0c7217cdde317cfec',
    'ubuntu-20': 'ami-0a0e5d9c7acc336f1'
  }
  return amis[os] || amis['amazon-linux-2']
}

