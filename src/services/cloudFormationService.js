/**
 * Exportar template como YAML (simplificado)
 */
export function exportTemplateAsYAML(template) {
  let yaml = `AWSTemplateFormatVersion: "${template.AWSTemplateFormatVersion}"
Description: ${template.Description}

Parameters:\n`

  for (const [key, value] of Object.entries(template.Parameters || {})) {
    yaml += `  ${key}:\n    Type: ${value.Type}\n`
    if (value.Default) yaml += `    Default: ${value.Default}\n`
    if (value.Description) yaml += `    Description: ${value.Description}\n`
    if (value.AllowedValues) yaml += `    AllowedValues: [${value.AllowedValues.join(', ')}]\n`
  }

  yaml += `\nResources:\n`
  for (const [key, resource] of Object.entries(template.Resources || {})) {
    yaml += `  ${key}:\n    Type: ${resource.Type}\n`
    if (resource.DependsOn) yaml += `    DependsOn: ${resource.DependsOn}\n`
    yaml += `    Properties:\n`
    yaml += objectToYaml(resource.Properties, 6)
  }

  if (template.Outputs && Object.keys(template.Outputs).length > 0) {
    yaml += `\nOutputs:\n`
    for (const [key, output] of Object.entries(template.Outputs)) {
      yaml += `  ${key}:\n    Description: ${output.Description}\n    Value: ${JSON.stringify(output.Value)}\n`
    }
  }

  return yaml
}

function objectToYaml(obj, indent) {
  const spaces = ' '.repeat(indent)
  let yaml = ''

  for (const [key, value] of Object.entries(obj)) {
    if (value === undefined || value === null) continue

    if (Array.isArray(value)) {
      yaml += `${spaces}${key}:\n`
      value.forEach(item => {
        if (typeof item === 'object') {
          yaml += `${spaces}-\n${objectToYaml(item, indent + 2)}`
        } else {
          yaml += `${spaces}- ${item}\n`
        }
      })
    } else if (typeof value === 'object') {
      yaml += `${spaces}${key}:\n${objectToYaml(value, indent + 2)}`
    } else if (typeof value === 'string' && value.includes('\n')) {
      yaml += `${spaces}${key}: |\n${value.split('\n').map(l => spaces + '  ' + l).join('\n')}\n`
    } else {
      yaml += `${spaces}${key}: ${value}\n`
    }
  }

  return yaml
}
